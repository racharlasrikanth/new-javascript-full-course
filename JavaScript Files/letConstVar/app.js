// LET VS CONST VS VAR

// Using Var
var someValue = "some data";
someValue = "new value added for var";

// Using Let
let firstName;
firstName = "Srikanth Racharla";

// Using Const  -   we cannot re-assign
const address = "HYD";
// address = "Hyderabad";

console.log(someValue);
console.log(firstName);
console.log(address);
