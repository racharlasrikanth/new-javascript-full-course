// Implicit Type Conversion

const firstName = "Srikanth";
const lastname = "Racharla";
const fullName = firstName + " " + lastname;
console.log(fullName);

const number1 = 10;
const number2 = 20;
const result = number1 + number2;
console.log(result);

let result2 = firstName - lastname;
console.log(result2);

let number3 = 10;
let number4 = "20";
let result3 = number3 - number4;
console.log(result3);
