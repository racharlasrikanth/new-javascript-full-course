// Conditional Statements
// Comparison Operators
// >, <, >=, <=, ==, ===, !=, !==
// == checks only value
// === checks value and type

const num1 = 10; // type=number
const num2 = "10"; // type=string

const value1 = num1 != num2;
const value2 = num1 !== num2;

console.log(value1);
console.log(value2);
